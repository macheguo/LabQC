    <h2>线性</h2>
    <p>
      <strong>线性</strong>是指检测在整个可报告范围内产生与目标物浓度成正比结果的能力。
      对于定量PCR，线性检测产生的Ct值随目标物浓度每增加一个对数单位而以一致的数量递减。
    </p>

    <h3>如何评估线性</h3>
    <ol>
      <li>配制覆盖预期可报告范围的系列稀释液（如 10<sup>1</sup> 至 10<sup>8</sup> copies/mL）。</li>
      <li>每种稀释液至少三复孔测量。</li>
      <li>将实测值（Ct或log copies）对预期值（已知浓度）作图。</li>
      <li>进行线性回归分析。</li>
    </ol>

