    <h2>第 7.7 条：质量控制的实施</h2>

    <h3>条款要求</h3>
    <p>
      7.7.1 条要求实验室实施 QC 程序以验证检验结果达到预期的质量。这是 ISO 15189 中质量控制的核心条款，
      评审员会重点审查。
    </p>

    <p>具体而言，实验室须：</p>
    <ol>
      <li>制定<strong>文件化的 QC 计划</strong>，包括检测的质控品类型、水平和频次。</li>
      <li>使用合适的<strong>质控品</strong>（优先使用独立于校准品的第三方质控品）。</li>
      <li>建立<strong>统计 QC 程序</strong>，使用如 Westgard 多规则的客观决策标准。</li>
      <li>定期<strong>评审 QC 数据</strong>，识别趋势和偏移。</li>
      <li>当 QC 不合格时采取<strong>纠正措施</strong>并记录。</li>
      <li>参与<strong>外部质量评估（EQA）/ 能力验证（PT）</strong>计划（7.7.2 条）。</li>
      <li>当 EQA 结果不满意时进行<strong>调查和纠正</strong>。</li>
      <li>建立实验室间<strong>比对</strong>机制，用于无 EQA 可用的检测项目。</li>
    </ol>

