      <tbody>
        <tr>
          <td>4.1 公正性</td>
          <td>识别和管理对实验室活动的风险</td>
          <td>Sigma 指标量化方法本身的"固有风险"</td>
        </tr>
        <tr>
          <td>7.7.1</td>
          <td>QC 程序应基于对方法性能的客观评估</td>
          <td>Sigma 值直接驱动 QC 规则和质控品数量选择</td>
        </tr>
        <tr>
          <td>7.7.3</td>
          <td>定期评审 QC 数据以识别潜在问题</td>
          <td>NMEDx 图可视化监控方法性能随时间变化</td>
        </tr>
        <tr>
          <td>8.5</td>
          <td>采取纠正措施防止问题复发</td>
          <td>趋势分析帮助识别需要纠正措施的临界方法</td>
        </tr>
      </tbody>
