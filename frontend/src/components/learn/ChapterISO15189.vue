    <p>
      例如，一个 Sigma 值为 2.5 的方法（"差"），本质上存在每百万次约 30 万次缺陷的风险。
      即使每次 QC 都通过，风险仍然存在。ISO 15189 要求实验室识别并管理这种风险——
      可能通过增加 QC 频次、使用多规则加 N=4 的增强策略，或选择更换为性能更好的方法。
    </p>

