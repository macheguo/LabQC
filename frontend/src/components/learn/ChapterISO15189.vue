        </tr>
      </thead>
      <tbody>
        <tr>
          <td>正确度（Trueness）</td>
          <td>测量平均值与参考值之间的一致性</td>
          <td>7.3.3 b)</td>
        </tr>
        <tr>
          <td>精密度（Precision）</td>
          <td>在规定条件下的测量结果一致程度</td>
          <td>7.3.3 c)</td>
