    <h2>第 7.3 条：方法验证与确认</h2>

    <h3>条款要求</h3>
    <p>
      7.3.1 条规定，实验室在引入检测方法用于临床检验之前，必须对其进行<strong>验证</strong>或
      <strong>确认</strong>，以证明该方法能够在实际使用条件下达到预期的性能。
    </p>
    <ul>
      <li><strong>验证（Verification）：</strong>适用于已由制造商确认的检验程序（如 CE-IVD 或 NMPA 批准的试剂盒）。
        实验室需证明其能够复现制造商声明的性能。</li>
      <li><strong>确认（Validation）：</strong>适用于实验室自建方法、修改过的标准方法、或超出预期用途使用的方法。
        要求更广泛的性能研究。</li>
    </ul>

    <h3>要求的性能参数</h3>
    <table>
